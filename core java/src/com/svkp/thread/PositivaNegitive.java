package com.svkp.thread;

public class PositivaNegitive {

	public static void main(String[] args) {
		Double number = 12.3  ;
		if(number > 0.0)
		{
			System.out.println(number +   " is a positive number");
		}
		else if(number < 0.0)
		{
			System.out.println(number +  "  is a negitive number");
		}
		else
		{
			System.out.println(number + "is a 0");
		}

	}

}
